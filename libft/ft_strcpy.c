#include "libft.h"

char	*ft_strcpy(char *str1, char *str2)
{
	char	*dst;
	int		i;

	i = 0;
	dst = str1;
	while (str2[i] != '\0')
	{
		dst[i] = str2[i];
		i++;
	}
	dst[i] = '\0';
	return (str1);
}

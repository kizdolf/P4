#include "../includes/i_a.h"

int		play_ia(t_puiss *p4, int col)
{
	int		i;

	i = p4->nb_lines - 1;
	while (i > 0 && p4->array[i][col] != 0)
		i--;
	if (p4->array[i][col] == 0)
	{
		p4->array[i][col] = NUM_IA;
		return (2);
	}
	return (0);
}
